                    </span>
				</li>
				<li>
					<span><strong>Consider your model carefully</strong> Each GPT model has different features and different pricing models, which can affect your experience using the plugin considerably. <br/><br/> <strong>GPT-4 </strong>is the very latest design, provides the best results and can handle much larger amounts of text. To date, we've found fewer issues using this model with AI-Scribe, but it's also the slowest and most expensive. For the GPT-4-8K model, requests can use a maximum of 8,192 tokens (approx. 6,000 words). <br/><br/> <strong>GPT-3</strong> (text-davinci-003) gives the most consistent results but the quality and accuracy will not be as good as GPT-4. Requests can use a maximum of 2,048 tokens (approx. 1,500 words) or a combined total of 4000 tokens for the request and response. For this reason, we tend to find that AI-Scribe can encounter token issues when producing longer articles or longer requests/prompt usage. <br/><br/> <strong>GPT-3.5-Turbo</strong> is by far the lowest cost but we've also experienced some of the most inconsistent results using this model. Requests can use a maximum of 4,096 tokens (approx. 3,000 words) but we've found that the results will also provide unwanted text, such as notes, labels and explanations. The text also seems to flag higher on AI content detection tools.&nbsp;</span>
